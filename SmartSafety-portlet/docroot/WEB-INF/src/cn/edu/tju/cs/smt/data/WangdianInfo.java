package cn.edu.tju.cs.smt.data;

public class WangdianInfo {
	public int id;
	public String name;
	public String address;
	public String yuandizhi;
	public String lnglat;

	public String getLnglat() {
		return lnglat;
	}

	public void setLnglat(String lnglat) {
		this.lnglat = lnglat;
	}

	public String phonenumber;
	public int rank;
	public Double rankresult;

	public Double getRankresult() {
		return rankresult;
	}

	public void setRankresult(Double rankresult) {
		this.rankresult = rankresult;
	}

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getYuandizhi() {
		return yuandizhi;
	}

	public void setYuandizhi(String yuandizhi) {
		this.yuandizhi = yuandizhi;
	}

	public String getPhonenumber() {
		return phonenumber;
	}

	public void setPhonenumber(String phonenumber) {
		this.phonenumber = phonenumber;
	}
}
